package com.loiane.cursojava.aula11;

public class VariaveisInteiras {

	public static void main(String[] args) {

			byte idade01 = 20;
			
			short idade02 = 21;
			
			int idade03 = 22;
			
			long idade04 = 23l;
			
			int decVal = 26;
			
			//int hexVal = 0x1a;
			int hexVal = 0x1A;
			
			int octVal = 032;
			
			int binVal = 0b11010; // JDK 7
			
			long creditCardNumber = 1234_5678_9012_3456L;
			
			long cpf = 101_134_156_68L;
			
			float pi = 3.14_15F;
			
			long hexBytes = 0xFF_EC_DE_5E;
			
			long hexWords = 0xCAFE_BABE;
			
			long maxLong = 0x7fff_ffff_ffff_ffffL;
			
			byte nybbles = 0b0010_0101;
			
			long bytes = 0b11010010_01101001_10010100_10010010;
			
			System.out.println("Valor variável idade01 = " + idade01);
			System.out.println("Valor variável idade02 = " + idade02);
			System.out.println("Valor variável idade03 = " + idade03);
			System.out.println("Valor variável idade04 = " + idade04);
			System.out.println("Valor variável decVal = " + decVal);
			System.out.println("Valor variável hexVal = " + hexVal);
			System.out.println("Valor variável octVal = " + octVal);
			System.out.println("Valor variável binVal = " + binVal);
			System.out.println("Valor variável creditCardNumber = " + creditCardNumber);
			System.out.println("Valor variável cpf = " + cpf);
			System.out.println("Valor variável pi = " + pi);
			System.out.println("Valor variável hexBytes = " + hexBytes);
			System.out.println("Valor variável hexWords = " + hexWords);
			System.out.println("Valor variável maxLong = " + maxLong);
			System.out.println("Valor variável nybbles = " + nybbles);
			System.out.println("Valor variável bytes = " + bytes);
			
	}

}
